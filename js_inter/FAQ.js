document.addEventListener('DOMContentLoaded', () => {
    const savedTheme = localStorage.getItem('preferred-theme') || 'light';
    const savedFontSize = localStorage.getItem('preferred-font-size') || 'medium';
    const savedFontStyle = localStorage.getItem('preferred-font-style') || "'Monotype Corsiva', cursive";
    const savedLanguage = localStorage.getItem('preferred-language') || 'en';

    if (savedTheme === 'dark') {
        document.body.classList.add('dark-theme');
    }
    
    document.body.classList.add(`font-${savedFontSize}`);
    document.body.style.fontFamily = savedFontStyle;
    
    if (window.applyLanguage) {
        window.applyLanguage(savedLanguage);
    }
});